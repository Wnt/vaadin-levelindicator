package org.sgx.raphael4gwt.raphael.base;

import com.google.gwt.core.client.JavaScriptObject;
/**
 * represents a native Font object returned by Paper.getFont. 
 * @author sg
 *
 */
public class Font extends JavaScriptObject {
	protected Font(){};
	public native final String getFamily()/*-{
		return this.family;
	}-*/;
	public native final String getWeight()/*-{
		return this.weight;
	}-*/;
	public native final String getStyle()/*-{
		return this.style;
	}-*/;
	public native final String getStretch()/*-{
		return this.stretch;
	}-*/;
}
